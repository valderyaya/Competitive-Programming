bool dfs(int now,int id, int s){
    if(now == 3) return 1;
    if(s == avr) return dfs(now+1,0,0);
    for(;id<n;++id){
        if(!a[id] || s+a[id]>avr) continue;
        int p = a[id]; a[id] = 0;
        if(dfs(now, id+1, s+p)) return 1;
        a[id] = p;
    }
    return 0;
}
