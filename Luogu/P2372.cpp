int ans,n,m,sx,sy,dx[]={1,1,0,-1,-1,-1,0,1},dy[]={0,1,1,1,0,-1,-1,-1},px[]={1,0,0,-1},py[]={0,1,-1,0};
string s[21];
queue<pii> q;
bool vis[21][21];
signed main(){
    cin>>n>>m>>sx>>sy;
    --sx,--sy;
    vis[sx][sy]=1;
    for(int i=0;i<n;++i) cin>>s[i];
    q.emplace(mkp(sx,sy));
    while(!q.empty()){
        auto [x,y]=q.front(); q.pop();
        int xx,yy;
        for(int i=0;i<8;++i){
            xx=x+dx[i], yy=y+dy[i];
            if(xx<0||xx>=n||yy<0||yy>=m||vis[xx][yy]||s[xx][yy]=='.') continue;
            q.emplace(mkp(xx,yy));
            vis[xx][yy]=1;
        }
        for(int i=0;i<4;++i){
            xx=x+px[i], yy=y+py[i];
            if(xx<0||xx>=n||yy<0||yy>=m||s[xx][yy]=='.') ++ans;
        }
    }
    cout<<ans;
}

